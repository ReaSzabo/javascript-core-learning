////// operátorok rövidítése

// ismerjük ezt a műveletet:
var x = 10;
x = x + 10;
console.log(x); // 20

// erre van egy rövidítés
var y = 10;
y += 10; // y = y + 10;
console.log(y); // 20

// nézzük meg szorzással
var z = 10;
z *= 2; // z = z * 2;
console.log(z); // 20

// nézzük meg osztással
var szam = 10;
szam /= 2; // szam = szam / 2;
console.log(szam); // 5

// nézzük meg kivonással
var dinnye = 30;
dinnye -= 15; // dinnye = dinnye - 15;
console.log(dinnye); // 15

// nézzük meg maradékos osztással
var torta = 11;
torta %= 2; // torta = torta % 2;
console.log(torta); // 1

// használhatunk változót is a jobb oldalon
var i = 10;
var j = 20;
i += j; // i = i + j;

