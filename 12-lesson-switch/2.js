var favoriteColor = "blue";

switch (favoriteColor) {
    case "red":
        console.log("Their favorite color is red.");
        break;
    case "blue":
        console.log("Their favorite color is blue.");
        break;
    case "green":
        console.log("Their favorite color is green.");
        break;
}

// This is the same as an if-else if statement

if (favoriteColor == "red") {
    console.log("Their favorite color is red.");
} else if (favoriteColor == "blue") {
    console.log("Their favorite color is blue.");
} else if (favoriteColor == "green") {
    console.log("Their favorite color is green.");
}
